async function run() {
  // cal_holidayの全イベントを取得
  const { data: events, error } = await sb
    .from('events')
    .select('id, title, note')
    .eq('calendar_id', 'cal_holiday');

  if (error) { console.error('取得失敗:', error.message); process.exit(1); }

  console.log(`取得: ${events.length}件\n`);

  let updated = 0;
  for (const ev of events) {
    // 振替は skip（例:「憲法記念日 振替」は対象外）
    const baseTitle = ev.title.replace(/\s*振替.*$/, '').trim();
    const note = NOTES[baseTitle];
    if (!note) {
      console.log(`  スキップ: ${ev.title}`);
      continue;
    }
    console.log(`  更新: ${ev.title}`);
    if (APPLY) {
      const { error: upErr } = await sb
        .from('events')
        .update({ note })
        .eq('id', ev.id);
      if (upErr) console.warn(`    警告: ${upErr.message}`);
      else updated++;
    }
  }

  if (!APPLY) {
    console.log('\n[DRY-RUN] --apply で実際に更新');
  } else {
    console.log(`\n✅ ${updated}件を更新しました`);
  }
}

run();
