export interface OrderMailContext {
  customer_name: string;
  order_id: string;
  total_yen: number;        // 税込合計 (税抜+消費税+送料)
  items_summary: string;     // "[3Mフィルム] SH2CLARX × 4m 等"
  bank_account?: string;     // 見積書送付時に振込先案内 (例: "PayPay銀行 ビジネス支店 普通 1234567 株式会社テクネスト")
  payment_deadline?: string; // 入金期限(YYYY-MM-DD or "5営業日以内")
  refund_amount?: number;    // 返金額(在庫切れ時)
  refund_extra?: number;     // 振込手数料相当(500円)
  ship_carrier?: string;     // ヤマト運輸/佐川急便
  ship_tracking?: string;    // 追跡番号
  ship_eta?: string;         // 到着予定
  cancel_reason?: string;    // キャンセル理由
}

